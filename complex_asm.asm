; =================================================================================================================
; complex double precision normal AVX2
; =================================================================================================================
; >>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>
;
; The math
;   result = e0*e0 + e1*e1
;
; -----------------------------------------------------------------------------------------------------------------
;
; notes:
;
; -----------------------------------------------------------------------------------------------------------------
;
; C prototype:
;
; void complex_norm64_AVX2(complex64 *c1, double *result);
;
; -----------------------------------------------------------------------------------------------------------------
;
public complex_norm64_AVX2
complex_norm64_AVX2 proc
    vmovapd      xmm0, xmmword ptr [rcx]                   ; xmm0 = c1e0, c1e1
    vmulpd       xmm0, xmm0, xmm0                          ; ymm0 = c1e0*c1e0, c1e1*c1e1
    vshufpd      xmm1, xmm0, xmm0, 1                       ; xmm1 = c1e1*c1e1, c1e0*c1e0
    vaddpd       xmm0, xmm1, xmm0                          ; xmm0 = c1e0*c1e0+c1e1*c1e1, c1e1*c1e1+c1e0*c1e0
    vmovq        qword ptr [rdx], xmm0                     ; result = xmm0
    ret
complex_norm64_AVX2 endp
; <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<
