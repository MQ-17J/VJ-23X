; =================================================================================================================
; complex double precision scalar multiply AVX2
; =================================================================================================================
; >>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>
;
; The math
;   result = e0*s + e1*s
;
; -----------------------------------------------------------------------------------------------------------------
;
; notes:
;
; -----------------------------------------------------------------------------------------------------------------
;
; C prototype:
;
; void complex_mul64s_AVX2(complex64 *c1, double *s, complex64 *result);
;
; -----------------------------------------------------------------------------------------------------------------
;
public complex_mul64s_AVX2
complex_mul64s_AVX2 proc
    vmovapd      xmm0, xmmword ptr [rcx]                   ; xmm0 = c1e0, c1e1
    vbroadcastsd ymm1, qword ptr [rdx]                     ; ymm1 = s, s, s, s (high lane is ignored)
    vmulpd       xmm0, xmm0, xmm1                          ; xmm0 = c1e0*s, c1e1*s
    vmovapd      xmmword ptr [r8], xmm0                    ; store result
    ret
complex_mul64s_AVX2 endp
; <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<
