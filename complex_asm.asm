public complex_add64_AVX2
complex_add64_AVX2 proc
    vmovapd     xmm0, xmmword ptr [rcx]                   ; xmm0 = c1e0, c1e1
    vaddpd      xmm0, xmm0, xmmword ptr [rdx]             ; xmm0 = c1e0+c2e0, c1e1+c2e1
    vmovapd     xmmword ptr [r8], xmm0                    ; result = xmm0
    ret
complex_add64_AVX2 endp
