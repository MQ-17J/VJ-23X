; =================================================================================================================
; complex double precision division AVX2
; =================================================================================================================
; >>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>
;
; The math
; ( c1e0*c2e0 + c1e1*c2e1)/c2norm -> e0
; (-c1e0*c2e1 + c1e1*c2e0)/c2norm -> e1
;
; -----------------------------------------------------------------------------------------------------------------
;
; notes:
;
; VSHUFPD
; (KL, VL) = (2, 128)
; IF IMM0[0] = 0
;     THEN TMP_DEST[63:0] := SRC1[63:0]
;     ELSE TMP_DEST[63:0] := SRC1[127:64] FI;
; IF IMM0[1] = 0
;     THEN TMP_DEST[127:64] := SRC2[63:0]
;     ELSE TMP_DEST[127:64] := SRC2[127:64] FI;
; FOR j := 0 TO KL-1
;     i := j * 64
;     IF k1[j] OR *no writemask*
;         THEN DEST[i+63:i] := TMP_DEST[i+63:i]
;         ELSE
;             IF *merging-masking* ; merging-masking
;                 THEN *DEST[i+63:i] remains unchanged*
;                 ELSE *zeroing-masking*
;                         ; zeroing-masking
;                     DEST[i+63:i] := 0
;             FI
;     FI;
; ENDFOR
; DEST[MAXVL-1:VL] := 0
;
; -----------------------------------------------------------------------------------------------------------------
;
; C prototype:
;
; void complex_div64_AVX2(complex64 *c1, complex64 *c2, complex64 *result);
;
; -----------------------------------------------------------------------------------------------------------------
;
public complex_div64_AVX2
complex_div64_AVX2 proc
    vxorpd       ymm0, ymm0, ymm0
    vxorpd       ymm1, ymm1, ymm1
    vmovapd      xmm0, xmmword ptr [rdx]                   ; xmm0 = c1e0, c1e1
    vmulpd       xmm0, xmm0, xmm0                          ; ymm0 = c1e0*c1e0, c1e1*c1e1
    vshufpd      xmm1, xmm0, xmm0, 1                       ; xmm1 = c1e1*c1e1, c1e0*c1e0
    vaddpd       xmm0, xmm1, xmm0                          ; xmm0 = c1e0*c1e0+c1e1*c1e1, c1e1*c1e1+c1e0*c1e0
    vmovapd      xmm4, xmm0                                ; xmm4 = xmm0 (c2norm)
    vmovapd      xmm0, xmmword ptr [rcx]                   ; xmm0 = c1e0, c1e1
    vmovapd      xmm2, xmmword ptr [rdx]                   ; xmm2 = c2e0, c2e1
    vshufpd      xmm1, xmm0, xmm0, 00000000b               ; xmm1 = c1e0, c1e0
    vmulpd       xmm1, xmm1, xmm2                          ; xmm1 = c1e0*c2e0, c1e0*c2e1
    vshufpd      xmm3, xmm0, xmm0, 00000011b               ; xmm3 = c1e1, c1e1
    vshufpd      xmm2, xmm2, xmm2, 00000001b               ; xmm2 = c2e1, c2e0
    vmulpd       xmm3, xmm3, xmm2                          ; xmm3 = c1e1*c2e1, c1e1*c2e0
    vfmadd132pd  xmm1, xmm3, xmmword ptr [i1]              ; xmm1 = (c1e0*c2e0)+((c1e1*c2e1)*i1[0]), (c1e0*c2e1)+((c1e1*c2e0)*i1[1])
    vdivpd       xmm1, xmm1, xmm4
    vmovapd      xmmword ptr [r8], xmm1
    ret
complex_div64_AVX2 endp
; <<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<<
